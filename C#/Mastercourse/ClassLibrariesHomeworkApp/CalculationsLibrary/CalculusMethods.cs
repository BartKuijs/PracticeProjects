using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace CalculationsLibrary;

public class CalculusMethods
{

    public double PlusMethod(double x, double y)
    {
        double output;
        output = x + y;
        return output;
    }

    public double MinusMethod(double x, double y)
    {
        double output;
        output = x - y;
        return output;
    }


}
